using System;

namespace P08._Divisible_by_3
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //Create for loop print number of range 0-100 divisible by 3
            for (int i = 3; i <= 100; i += 3)
            {
                //Print result 
                Console.WriteLine(i);
            }
        }
    }
}
